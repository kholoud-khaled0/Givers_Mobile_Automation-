package utils.common;

public class CommunityStats {

    private String communityType;

    private String createdNeedyCount;
    private String supportedNeedyCount;
    private String donationsAmount;
    private String giversCount;
    private String followersCount;

    public String getCommunityType() {
        return communityType;
    }

    public void setCommunityType(String communityType) {
        this.communityType = communityType;
    }

    public String getCreatedNeedyCount() {
        return createdNeedyCount;
    }

    public void setCreatedNeedyCount(String createdNeedyCount) {
        this.createdNeedyCount = createdNeedyCount;
    }

    public String getSupportedNeedyCount() {
        return supportedNeedyCount;
    }

    public void setSupportedNeedyCount(String supportedNeedyCount) {
        this.supportedNeedyCount = supportedNeedyCount;
    }

    public String getDonationsAmount() {
        return donationsAmount;
    }

    public void setDonationsAmount(String donationsAmount) {
        this.donationsAmount = donationsAmount;
    }

    public String getGiversCount() {
        return giversCount;
    }

    public void setGiversCount(String giversCount) {
        this.giversCount = giversCount;
    }

    public String getFollowersCount() {
        return followersCount;
    }

    public void setFollowersCount(String followersCount) {
        this.followersCount = followersCount;
    }


}
